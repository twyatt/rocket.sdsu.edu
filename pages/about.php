	<h1>About the San Diego State Rocket Project,</h1>
	
	The San Diego State University (SDSU) Rocket Project was originally founded in May 2003 and the
original rocket team designed, fabricated, and launched 4 rockets. The initial design and fabrication of
the Rocket Project's 5th rocket began in 2008, but was never fully completed. In fall of 2011, the Rocket
Project was picked up once again by engineering students with the goal of finally completing the rocket.
Within one year we expanded the Rocket Project from a team of 5 students to 45 students, culminating
in the successful launch and recovery of our 18 foot, liquid fueled rocket. Take a look at our <a href="http://sdsurocketproject.blogspot.com/" target="_blank">blog</a> to
follow the SDSU Rocket Project's current projects and the progress we have been making.

		<p class="rocket_text">
		On October 20, 2012 the San Diego State University Rocket Project
		launched a liquid propellant rocket near a dry lakebed in the Mojave
		Desert. Over 25 engineering students were in attendance. The student
		designed & built rocket was over 18 feet tall. At approximately 3:00 in
		the afternoon the rocket roared to life and lifted off on a column of
		flames under clear blue skies and a strong breeze. The rocket
		accelerated upward until it was just about out of sight. It reached a
		peak velocity of almost 600 mph when the engine stopped and coasted to a
		peak altitude over 10,000 feet. A system of parachutes was deployed to
		bring the rocket safely back to the ground. This was the culmination of
		months of hard work by this enthusiastic and dedicated group of
		engineering students.
		</p>

